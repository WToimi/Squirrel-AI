    private void CreateLevel()
    {

        Vector2 Position = new Vector2(0, 0);
        for (int i = 0; i < Mapplan.GetLength(0); i++)
        {
            for (int p = 0; p < Mapplan.GetLength(1); p++)
            {
                Position.x += gräs.transform.lossyScale.x / 3;
                if (Mapplan[i, p] == 1)
                {
                    GameObject _gräs = (GameObject)Instantiate(gräs, Position, transform.rotation);
                }
                
                else if (Mapplan[i, p] == 2)
                {
                    GameObject _vatten = (GameObject)Instantiate(vatten, Position, transform.rotation);

                }
                else if (Mapplan[i, p] == 3)
                {
                    GameObject _mat = (GameObject)Instantiate(mat, Position, transform.rotation);
                }
                else if (Mapplan[i, p] == 4)
                {
                    GameObject _gräs = (GameObject)Instantiate(gräs, Position, transform.rotation);
                    GameObject _liv = (GameObject)Instantiate(liv, new Vector3(Position.x,Position.y,-1), transform.rotation);
                }
                else if (Mapplan[i, p] == 0)
                {
                    GameObject _sten = (GameObject)Instantiate(sten, Position, transform.rotation);
                }

            }
            Position.x = 0f;
            Position.y += gräs.transform.lossyScale.y / 3;


        }


    }
